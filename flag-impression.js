// api/orders/create.js
// Called by Stripe webhook after successful payment
// Creates the customer (or finds existing) and creates the order

import { createClient } from '@supabase/supabase-js';

const supabase = createClient(
  process.env.SUPABASE_URL,
  process.env.SUPABASE_SERVICE_KEY
);

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  const {
    first_name, last_name, email, phone,
    addr_line1, addr_line2, city, state, zip, country,
    option, color, sport, notes,
    amount_cents, payment_method, payment_id
  } = req.body;

  // Basic validation
  if (!email || !first_name || !last_name || !addr_line1 || !city || !state || !zip) {
    return res.status(400).json({ error: 'Missing required fields' });
  }

  try {
    // 1. Upsert customer — if email exists, update address; else create new
    const { data: customer, error: customerError } = await supabase
      .from('customers')
      .upsert(
        { first_name, last_name, email, phone, addr_line1, addr_line2, city, state, zip, country: country || 'US' },
        { onConflict: 'email', ignoreDuplicates: false }
      )
      .select()
      .single();

    if (customerError) throw customerError;

    // 2. Create order (order_number auto-generated by DB trigger)
    const { data: order, error: orderError } = await supabase
      .from('orders')
      .insert({
        customer_id:     customer.id,
        order_number:    '',  // trigger fills this in
        stage:           1,
        option,
        color,
        amount_cents,
        payment_method,
        payment_id,
        logo_requested:  option === 'logo',
        sport,
        notes
      })
      .select()
      .single();

    if (orderError) throw orderError;

    // 3. Log the initial event
    await supabase.from('order_events').insert({
      order_id:   order.id,
      from_stage: null,
      to_stage:   1,
      changed_by: 'admin',
      note:       'Order created — kit queued for dispatch'
    });

    return res.status(200).json({
      success: true,
      order_number: order.order_number,
      order_id:     order.id
    });

  } catch (err) {
    console.error('Create order error:', err);
    return res.status(500).json({ error: 'Failed to create order', detail: err.message });
  }
}
